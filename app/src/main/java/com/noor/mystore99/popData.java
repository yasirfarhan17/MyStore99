package com.noor.mystore99;

public class popData {
    String weight,rate;

    public popData(){

    }

    public popData(String weight, String rate) {
        this.weight = weight;
        this.rate = rate;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }
}
